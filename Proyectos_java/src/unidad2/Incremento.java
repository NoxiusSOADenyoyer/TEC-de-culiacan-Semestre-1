package unidad2;

public class Incremento {

	public static void main(String[] args) {
		int m  = 45 , n= 75 ;
		System.out.println("m = " + m + " n = " + n);
		++m;
		--n;
		System.out.println("m = " + m + " n = " + n);
		m++;
		n--;
		System.out.println("m = " + m + " n = " + n);

	}

}
